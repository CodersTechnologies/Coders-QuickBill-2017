    public void saveTC(){
        try{
            String sql="Update termsConditions set tcDescription=? where tcID='1'";
            pst=conn.prepareStatement(sql);
            pst.setString(1, txtArea.getText());
            pst.execute();
            pst.close();
            JOptionPane.showMessageDialog(null, "Terms and Conditions updated.","Congrats",JOptionPane.INFORMATION_MESSAGE);
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, e,"saveTC() Exception",JOptionPane.ERROR_MESSAGE);
        }
    }

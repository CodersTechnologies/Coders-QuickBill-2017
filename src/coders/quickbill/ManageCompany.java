    //Program to Add Company Details
    public void addCompany(){
        try{
            String sql="insert into Company (c_name,c_address,c_contact,c_email,c_gstin,compid)values(?,?,?,?,?,?)";
            pst=conn.prepareStatement(sql);
            pst.setString(1, txtCName.getText());
            pst.setString(2, txtAddress.getText());
            pst.setString(3, txtContact.getText());
            pst.setString(4, txtEmail.getText());
            pst.setString(5, txtGSTIN.getText());
            pst.setInt(6, '1');
            pst.execute();
            JOptionPane.showMessageDialog(null, "Congrats! Your Company has been added successfully.","Company Added",JOptionPane.INFORMATION_MESSAGE);
            pst.close();
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, e,"addCompany() Exception",JOptionPane.ERROR_MESSAGE);
        }
    }

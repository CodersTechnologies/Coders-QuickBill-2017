    //Program to read db on Window Opened
    public void readDB(){
        try{
            String sql="Select * from Company";
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery();
            if(rs.next()){
                txtCName.setText(rs.getString("c_name"));
                txtAddress.setText(rs.getString("c_address"));
                txtContact.setText(rs.getString("c_contact"));
                txtEmail.setText(rs.getString("c_email"));
                txtGSTIN.setText(rs.getString("c_gstin"));
                btnAdd.setVisible(false);
                btnUpdate.setVisible(true);
            }
            else{
                txtCName.setText("");
                txtAddress.setText("");
                txtContact.setText("");
                txtEmail.setText("");
                txtGSTIN.setText("");
                btnAdd.setVisible(true);
                btnUpdate.setVisible(false);
                txtCName.requestFocus();
            }
            pst.close();
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, e,"readDB() Exception",JOptionPane.ERROR_MESSAGE);
        }
    }

    //Program to delete existing Customer from database
    public void delDealer(){
        if(!"".equals(txtDID.getText())){
           try{
               String sql="Delete from Dealer where dID='"+txtDID.getText()+"'";
               pst=conn.prepareStatement(sql);
               pst.execute();
               JOptionPane.showMessageDialog(null,"Data deleted from database.");
               getDealerDataToTable();
               getDealerData();
               pst.close();
           }
           catch(SQLException | HeadlessException e){
               JOptionPane.showMessageDialog(null, e);
           }
       }
       else{
           JOptionPane.showMessageDialog(null, "Dealer not selected.");
       }
    }

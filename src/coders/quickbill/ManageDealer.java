    //Program to get table data to field
    public void getTableToField(){
        try{
            int row=tblDealerMgmtPnl.getSelectedRow();
            String tblClick=tblDealerMgmtPnl.getModel().getValueAt(row, 0).toString();
            String sql="Select * from Dealer where dID='"+tblClick+"'";
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery();
            if(rs.next()){
                txtDID.setText(rs.getString("dID"));
                txtDealerName.setText(rs.getString("dName"));
                txtGSTIN.setText(rs.getString("dGSTIN"));  
                txtAddress.setText(rs.getString("dAddress"));
                txtContact.setText(rs.getString("dContact"));
                txtEmail.setText(rs.getString("dEmail"));
            }
            pst.close();
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
    }

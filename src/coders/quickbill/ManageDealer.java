    public void updtDealer(){
        if(!"".equals(txtDID.getText()) && !"".equals(txtDealerName.getText()) && !"".equals(txtGSTIN.getText())&& !"".equals(txtAddress.getText())){
            try{
                String sql="Update Dealer set dName=?, dGSTIN=?, dAddress=?, dContact=?, dEmail=? where dID='"+txtDID.getText()+"'";
                pst=conn.prepareStatement(sql);
                pst.setString(1, txtDealerName.getText());
                pst.setString(2, txtGSTIN.getText());
                pst.setString(3, txtAddress.getText());                
                pst.setString(4, txtContact.getText());
                pst.setString(5, txtEmail.getText());
                pst.execute();                 
                JOptionPane.showMessageDialog(null, "Data updated on database.");
                pst.close();
            }
            catch(SQLException | HeadlessException e){
                JOptionPane.showMessageDialog(null, e);
            }
       }
        else{
            JOptionPane.showMessageDialog(null, "Empty data cannot be saved.");
        }
    }

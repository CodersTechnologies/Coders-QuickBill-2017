    int count=0;
    public void getDealerData(){
        try{            
            String sql="Select * from Dealer";
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery();
            while(rs.next()){
                count=rs.getInt("dID");                
            }
            count++;
            txtDID.setText(String.valueOf(count));
            txtDealerName.setText("");
            txtGSTIN.setText("");
            txtAddress.setText("");
            txtContact.setText("");
            txtEmail.setText("");
            pst.close();
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
    }

    //Program to add new Customer to database
    public void addDealer(){
        if(!"".equals(txtDealerName.getText()) && !"".equals(txtGSTIN.getText()) && !"".equals(txtAddress.getText())){
            try{
                String sql="Insert into Dealer (dName,dGSTIN,dAddress,dContact,dEmail)values(?,?,?,?,?)";
                pst=conn.prepareStatement(sql);
                pst.setString(1, txtDealerName.getText());
                pst.setString(2, txtGSTIN.getText());
                pst.setString(3, txtAddress.getText());
                pst.setString(4, txtContact.getText());
                pst.setString(5, txtEmail.getText());
                pst.execute();
                pst.close();
                JOptionPane.showMessageDialog(null, "Dealer added to database.");
            }   
            catch(SQLException | HeadlessException e){
                JOptionPane.showMessageDialog(null, e);
            }
            
        }
        else{
            JOptionPane.showMessageDialog(null, "Cannot save empty data.","STOP",JOptionPane.ERROR_MESSAGE);
        }  
    }

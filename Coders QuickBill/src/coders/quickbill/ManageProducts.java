    //Program to read Products Database
    public void readProducts(){
        try{
            String sql="Select pid as 'Product ID',p_barcode as 'Barcode',p_hsncode as 'HSN Code',p_itemName as 'Item Name',p_description as 'Description',"
                    + "p_qty as 'Quantity',p_unit as 'Unit',p_rate as 'Rate',p_cgst as 'CGST',p_sgst as 'SGST',p_igst as 'IGST' from Products";
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery();
            tblManageProducts.setModel(DbUtils.resultSetToTableModel(rs));
            pst.close();            
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, e,"readProducts() Exception",JOptionPane.ERROR_MESSAGE);
        }
    }
    //Program to set new Product ID
    int pid=0;
    public void setNewPID(){
        try{
            String sql="Select * from Products";
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery();
            while(rs.next()){
                pid=rs.getInt("pid");
            }
            pid++;
            txtProductID.setText(String.valueOf(pid));
            txtBarcode.setText("");
            txtHSNCODE.setText("");
            txtItemName.setText("");
            txtDescription.setText("");
            txtQty.setText("0");
            cmbUnit.setSelectedIndex(0);
            txtRate.setText("0");
            txtCGST.setText("0");
            txtSGST.setText("0");
            txtIGST.setText("0");
            txtBarcode.requestFocus();
            pst.close(); 
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, e,"setPID() Exception",JOptionPane.ERROR_MESSAGE);
        }
    }

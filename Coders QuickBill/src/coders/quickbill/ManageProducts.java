    //Program to add Product to database
    public void addProduct(){
        try{
            String sql="insert into Products(p_barcode,p_itemName,p_description,p_qty,p_unit,p_rate,p_cgst,p_sgst,p_igst,p_hsncode)"
                    + "values(?,?,?,?,?,?,?,?,?,?)";
            pst=conn.prepareStatement(sql);
            pst.setString(1,txtBarcode.getText());
            pst.setString(2,txtItemName.getText());
            pst.setString(3, txtDescription.getText());
            pst.setString(4, txtQty.getText());
            pst.setString(5, cmbUnit.getSelectedItem().toString());
            pst.setString(6, txtRate.getText());
            pst.setString(7, txtCGST.getText());
            pst.setString(8, txtSGST.getText());
            pst.setString(9, txtIGST.getText());
            pst.setInt(10, Integer.parseInt(txtHSNCODE.getText()));
            pst.execute();
            pst.close();
            JOptionPane.showMessageDialog(null, "Product Added to Database Successfully","Product Saved",JOptionPane.INFORMATION_MESSAGE);
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, e,"addProduct() Exception",JOptionPane.ERROR_MESSAGE);
        }
    }

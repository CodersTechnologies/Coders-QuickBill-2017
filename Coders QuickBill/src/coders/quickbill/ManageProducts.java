    //Program on barcode scan
    public void barcodeScan(){
        try{
            String sql="Select * from Products where p_barcode='"+txtBarcode.getText()+"'";
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery();
            if(rs.next()){
                txtProductID.setText(rs.getString("pid"));
                txtBarcode.setText(rs.getString("p_barcode"));
                txtHSNCODE.setText(rs.getString("p_hsncode"));
                txtItemName.setText(rs.getString("p_itemName"));
                txtDescription.setText(rs.getString("p_description"));
                txtQty.setText(rs.getString("p_qty"));
                cmbUnit.setSelectedItem(rs.getString("p_unit"));
                txtRate.setText(rs.getString("p_rate"));
                txtCGST.setText(rs.getString("p_cgst"));
                txtSGST.setText(rs.getString("p_sgst"));
                txtIGST.setText(rs.getString("p_igst"));
                btnAdd.setEnabled(false);
                btnUpdate.setEnabled(true);
                btnDelete.setEnabled(true);                
            }
            else{
                //txtBarcode.setText("");
                txtHSNCODE.setText("");
                txtItemName.setText("");
                txtDescription.setText("");
                txtQty.setText("0");
                cmbUnit.setSelectedItem("--Select Unit--");
                txtRate.setText("0");
                txtCGST.setText("0");
                txtSGST.setText("0");
                txtIGST.setText("0");
                btnAdd.setEnabled(true);
                btnUpdate.setEnabled(false);
                btnDelete.setEnabled(false);
                JOptionPane.showMessageDialog(null, "Barcode not found. Add new to record.","ATTENTION!",JOptionPane.INFORMATION_MESSAGE);
            }   
            pst.close();
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, e,"barcodescan() Exception",JOptionPane.ERROR_MESSAGE);
        }
    }
